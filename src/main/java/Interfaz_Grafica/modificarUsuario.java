    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Conn c = new Conn();
        UserManagement um = new UserManagement();
        User u = getUserData();
        u.setPassword(passwordTxt.getText());
        u.setEmail(emailTxt.getText());
        u.setGender(genderTxt.getText());
        u.setAge(parseInt(ageTxt.getText()));
        u.setWeight(parseDouble(weightTxt.getText()));
        u.setHeight(parseDouble(heightTxt.getText()));
       
        if(um.Update(c.Connect(), u)==true){
            JOptionPane.showMessageDialog(null, "Datos modificados Exitosamente");
            dispose();
        }
        else{JOptionPane.showMessageDialog(null, "No se modificaron los datos");}
        
    }//GEN-LAST:event_jButton1ActionPerformed

