    public void ShowUserData(){
        Conn c  = new Conn();
        UserManagement um = new UserManagement();
        User u= um.Search(c.Connect(), getUser());
        showUserData(u);
    }
    public void showUserData(User u){
        userNameTxt.setText(u.getName());
        ageTxt.setText(String.valueOf(u.getAge()));
        genderTxt.setText(u.getGender());
        emailTxt.setText(u.getEmail());
        weightTxt.setText(String.valueOf(u.getWeight()));
        heightTxt.setText(String.valueOf(u.getHeight()));
        passwordTxt.setText(u.getPassword());
        userNameTxt.setEditable(false);
    }

